    <section class="global-container" id="2">
        <span class="fl"> II. Les conditions générales d'utilisation (CGU)s</span>
        <div class="clear outline"><b>CGU</b><br>Parfois obligatoires, selon l'activité d'un site internet, les CGU ou
            conditions générales d'utilisation sont essentielles pour l'éditeur d'un site Web, quel qu'il soit.
            Véritable contrat entre éditeur et visiteur du site Web, qu'il soit un membre inscrit ou non, ce sont elles
            qui vont régir tous les rapports et les conflits qui peuvent naître de l'existence de ce site.
            <br><br>
            En tant qu'éditeur, vous vous exposez souvent à des poursuites, sans même le savoir. Dieu merci, le plus
            souvent, les conflits se règlent à l'amiable, mais il peut arriver que les choses n'en restent pas là.
    </section>

    <section class="global-container" id="3">
        <span class="fl"> II-A. Les informations données par vos CGU</span>
        <div class="clear outline"><b>CGU</b><br>Les conditions générales d'utilisation n'ont heureusement pas pour
            unique vocation de gérer les conflits, elles permettent ainsi de fournir aux visiteurs certaines
            informations obligatoires ou non sur votre site et votre société.
            <br><br>
            Dans les CGU, vous pouvez ainsi faire mention des informations légales sur votre société ou association,
            adresse du siège, contacts etc. Dans cette rubrique, vous pouvez également prévoir une présentation plus
            détaillée de votre activité.
            <br><br>
            De même, c'est dans les CGU que vous pourrez définir l'objectif du site et surtout ses conditions
            d'utilisations. Les CGU vont être, pour tout le site, comme un règlement intérieur, pouvant même prévoir des
            sanctions comme l'exclusion d'un membre ou la suspension de ses droits. Par la suite, toute expulsion d'un
            membre devenu indésirable sera justifiée par l'application des CGU.
            <br><br>
            De même, sur certains sites dont le contenu (violent ou érotique) serait de nature à heurter les visiteurs
            les plus sensibles, au-delà d'une restriction d'accès à l'entrée, une mention dans les CGU vous permet de
            justifier de l'accomplissement de vos obligations.
    </section>

    <section class="global-container" id="4">
        <span class="fl"> II-B. Les garanties juridiques que vous apportent vos CGU</span>
        <div class="clear outline"><b>CGU</b><br>
            Sur un plan plus juridique, les CGU vont être pour vous un véritable
            bouclier contre grand nombre d'actions judiciaires.
            <br><br>
            Vous pouvez ainsi limiter votre responsabilité quant au contenu du site, aux opinions qui y sont évoquées
            par vos visiteurs, mais également quant à son bon fonctionnement (qui n'est pas toujours garanti). Vos CGU
            peuvent également vous permettre de prévoir quelle juridiction sera compétente en cas de litige (et vous
            évitera donc d'aller plaider à Marseille si le siège de votre société est à Lille). Les CGU vous permettront
            de faire mention des réserves de droit de propriété intellectuelle, notamment quant aux articles et images
            que vous publiez et vos conditions générales seront la base et le fondement d'actions futures.
            <br><br>
            Le respect de la loi "informatique et libertés" doit également être assuré sur votre site, notamment par
            rapport à la collecte de données personnelles sur les membres inscrits. Les conditions d'application de ce
            respect doivent être mentionnées dans vos CGU ou dans une rubrique prévue à cet effet.
            <br>
            <br>
            Enfin, certains domaines d'activité sont réglementés et les conditions de mise en oeuvre du respect de
            toutes ces réglementations doit figurer en bonne place dans vos CGU.
    </section>


    <section class="global-container" id="5">
