        <div class="clear outline"><b>CGV</b><br> Les conditions générales de vente (CGV) sont indispensables à TOUS les
            professionnels dont l'activité est la vente (de biens ou de services). En effet, elles constituent la base
            contractuelle de toutes les relations commerciales entre vendeur et acheteur, que ce dernier soit
            professionnel ou non.
            <p>
                Les CGV vont différer selon la nature juridique de l'acheteur mais la loi laisse néanmoins une bonne
                marge
