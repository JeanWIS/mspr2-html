            </p>
    </section>

    <section class="global-container" id="6">
        <span class="fl"> III-A. Les CGV dans les relations entre professionnels.</span>
        <div class="clear outline"><b>CGU</b><br>Définies à l'article L411-6 du code de commerce, les conditions
            générales de vente dans les relations entre professionnels doivent impérativement comporter certaines
            informations.

            <ul>
                <li>
                    Les conditions de la vente.
                </li>
                <li>
                    Les tarifs applicables, leur mode de calcul.
                </li>
                <li>
                    Les éventuelles réductions, remise et ristournes, ainsi que leur mode de calcul.
                </li>
                <li>
                    Les conditions de règlement, les délais et les sanctions.
                </li>
                <li>
                    Les conditions de règlement, les délais et les sanctions.
                </li>
            </ul>
            <p>
                Ces mentions obligatoires ne sont pas limitatives et il est possible et même conseillé de préciser une
                multitude d'autres détails qui permettront au professionnel vendeur de se couvrir par la suite contre
                d'éventuelles actions de ses clients.
            </p>
            <p>
                Ainsi, il est possible de faire figurer toutes les mentions relatives aux garanties des biens et
                services vendus, leur éventuelle application, les délais et les formes. De même, les modalités de
                livraison peuvent être précisées dans cette rubrique. Le vendeur peut également prévoir, ce qui est
                possible en droit commercial, une clause d'attribution de compétence. Il pourra ainsi déterminer quelle
                seront les juridictions compétentes à trancher les litiges nés de l'exécution du contrat de vente.
                (Cette clause est néanmoins limitée, tant matériellement que territorialement).
            </p>
    </section>

    <section class="global-container" id="7">
        <span class="fl"> III-B. Les CGV dans les relations entre le vendeur professionnel et le consommateur.</span>
        <div class="clear outline"><b>CGV</b><br> La protection du consommateur a pris une place prépondérante dans
            notre économie, cependant, le législateur a laissé une certaine marge de manoeuvre aux vendeurs
            professionnels, notamment dans l'établissement de certaines clauses.
            <p>
                Par contre, la communication des conditions générales de vente est rendue obligatoire, sous peine de
                sanctions assez lourdes, par les articles L111-1 du code de la consommation.


            </p>
            <p>
                Ainsi, il conviendra bien évidemment de reprendre les mentions les plus évidentes comme les conditions
                de vente et de paiement, les tarifs et les réductions éventuelles, de même que les modalités
                d'application que la garantie.


            </p>

            <p>
                Le contrôle du caractère abusif de certaines clause est effectué par le juge compétent, ou à défaut, par
                une commission créée à cet effet. Cependant, afin d'éviter les clauses abusives, le législateur en a
                cité un certain nombre, en annexe au code de la consommation. Cette liste n'est pas limitative et sera,
                en règle générale, considérée comme clause abusive toute clause tendant à rendre les rapports entre
                professionnel vendeur et consommateur déséquilibrés au détriment du consommateur (si par exemple les CGV
                prévoient une indemnité en cas de rupture unilatérale du contrat, uniquement au profit du
                professionnel).


            </p>
            <p>
                Enfin, dans le cadre des ventes à distance (ce qui est nécessairement le cas sur internet), la
                protection du consommateur a dicté certaines règles au législateur. La plus connue d'entre elles est
                bien sûr l'existence d'un délai de rétractation de sept jours et certaines mentions devenues obligatoire
                par l'application de la loi Chatel du 3 janvier 2008, comme notamment la mention exacte des délais de
                livraison, quel que soit le montant du contrat, mais aussi les modalités de remboursement, le cas
                échéant, ou encore les modalités d'exercice du droit de rétractation.


            </p>

            <p>
                Ces règles peuvent être rappelées dans les conditions générales de vente.

            </p>
    </section>

